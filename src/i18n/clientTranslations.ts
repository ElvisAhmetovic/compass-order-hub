const translations: Record<ClientLanguage, TranslationDict> = { en, de, nl, fr, sv };

export function getTranslation(language: ClientLanguage, key: string, params?: Record<string, string>): string {
  const dict = translations[language] || translations.en;
  let value = dict[key] || translations.en[key] || key;
  
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      value = value.replace(`{${k}}`, v);
    });
  }
  
  return value;
}

export default translations;
